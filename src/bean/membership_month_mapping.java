package bean;

public class membership_month_mapping {
	
	private int month_id;
	private int mcard_id;
	private int price;
	private int month_number;
	
	public int getMonth_id() {
		return month_id;
	}
	public void setMonth_id(int month_id) {
		this.month_id = month_id;
	}
	public int getMcard_id() {
		return mcard_id;
	}
	public void setMcard_id(int mcard_id) {
		this.mcard_id = mcard_id;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getMonth_number() {
		return month_number;
	}
	public void setMonth_number(int month_number) {
		this.month_number = month_number;
	}
	
	

}
